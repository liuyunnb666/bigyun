-- =============================================
-- 删除旧版配置中心菜单
-- =============================================
-- 说明：
-- 1. 旧版菜单位于"系统工具"目录下
-- 2. 新版菜单已迁移到"配置管理"一级菜单
-- 3. 删除旧版菜单避免用户混淆
-- =============================================

-- 删除旧版配置菜单及其子菜单和按钮权限
DELETE FROM sys_menu WHERE menu_id IN (
    106,   -- 系统工具 > 配置中心
    117,   -- 系统工具 > Provider管理
    118,   -- Provider配置
    1181,  -- Provider配置 > 查询
    1182,  -- Provider配置 > 新增
    1183,  -- Provider配置 > 修改
    1184,  -- Provider配置 > 删除
    1185   -- Provider配置 > 导出
);

-- 验证删除结果
SELECT
    menu_id,
    menu_name,
    parent_id,
    order_num,
    path
FROM sys_menu
WHERE menu_id IN (106, 117, 118, 1181, 1182, 1183, 1184, 1185);

-- 如果上面的查询返回空结果，说明删除成功
